function initLightbox() {
  const grid = document.getElementById("gallery-grid");
  const lightbox = document.getElementById("lightbox");
  if (!grid || !lightbox) return;

  const thumbs = [...grid.querySelectorAll(".gallery-thumb")];
  const imgEl = lightbox.querySelector(".lightbox-img");
  const closeBtn = lightbox.querySelector(".lightbox-close");
  const prevBtn = lightbox.querySelector(".lightbox-prev");
  const nextBtn = lightbox.querySelector(".lightbox-next");
  const sources = thumbs.map((thumb) => {
    const img = thumb.querySelector("img");
    return { src: img.currentSrc || img.src, alt: img.alt };
  });
  let currentIndex = 0;
  let touchStartX = 0;

  function updateImage() {
    const { src, alt } = sources[currentIndex];
    imgEl.src = src;
    imgEl.alt = alt;
  }

  function open(index) {
    currentIndex = index;
    updateImage();
    lightbox.hidden = false;
    lightbox.setAttribute("aria-hidden", "false");
    document.body.style.overflow = "hidden";
  }

  function close() {
    lightbox.hidden = true;
    lightbox.setAttribute("aria-hidden", "true");
    document.body.style.overflow = "";
  }

  function go(delta) {
    currentIndex = (currentIndex + delta + sources.length) % sources.length;
    updateImage();
  }

  thumbs.forEach((thumb, index) => {
    thumb.addEventListener("click", () => open(index));
  });

  closeBtn.addEventListener("click", close);
  prevBtn.addEventListener("click", () => go(-1));
  nextBtn.addEventListener("click", () => go(1));

  lightbox.addEventListener("click", (event) => {
    if (event.target === lightbox) close();
  });

  document.addEventListener("keydown", (event) => {
    if (lightbox.hidden) return;
    if (event.key === "Escape") close();
    if (event.key === "ArrowLeft") go(-1);
    if (event.key === "ArrowRight") go(1);
  });

  lightbox.addEventListener(
    "touchstart",
    (event) => {
      touchStartX = event.changedTouches[0].screenX;
    },
    { passive: true }
  );

  lightbox.addEventListener(
    "touchend",
    (event) => {
      const delta = event.changedTouches[0].screenX - touchStartX;
      if (Math.abs(delta) < 50) return;
      go(delta > 0 ? -1 : 1);
    },
    { passive: true }
  );
}

initLightbox();

function initFooterVisibility() {
  const contactSection = document.querySelector("#contacto");
  const footer = document.querySelector("#site-footer");
  if (!contactSection || !footer) return;

  const observer = new IntersectionObserver(
    ([entry]) => {
      const contactRect = contactSection.getBoundingClientRect();
      const footerVisible = contactRect.bottom <= window.innerHeight;
      footer.classList.toggle("is-hidden", entry.isIntersecting && !footerVisible);
    },
    { threshold: [0, 0.2, 0.5, 0.8, 1] }
  );

  observer.observe(contactSection);
}

initFooterVisibility();
